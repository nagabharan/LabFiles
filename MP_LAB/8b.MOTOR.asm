.MODEL SMALL
.DATA
N DW 400
.CODE
MOV AX,@DATA
MOV DS,AX
MOV DX,9803H
MOV AL,80H
OUT DX,AL
MOV DX,9800H
MOV AL,88H
MOV CX,N;
AGAIN: OUT DX,AL
   CALL DELAY
   ROR AL,01
   LOOP AGAIN
   MOV CX,N
AGAIN1: OUT DX,AL
        CALL DELAY
        ROL AL,01
        LOOP AGAIN1
        MOV AH,4CH
        INT 21H
DELAY PROC
        PUSH CX
        PUSH BX
        MOV CX,0FFFH
LOOP1: MOV BX,0FFFH
LOOP2: DEC BX
JNZ LOOP2
LOOP LOOP1
POP BX
POP CX
RET
DELAY ENDP
END
